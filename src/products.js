let renderList = document.querySelector(".main");
let select = document.querySelector("#select");
let productsLi = document.querySelector("#pro");

select.addEventListener("change", () => {
  let selectValue = select.value;

  const filterProduct = products.filter((product) => {
    return product.name === selectValue;
  });

  articles.innerHTML = "";
  filterProduct.forEach((element) => {
    renderList.append(render(element));
  });
});

let articles = document.createElement("div");
articles.className = "articles-div";

const render = (product) => {
  let card = document.createElement("div");
  let name = document.createElement("h3");
  name.innerText = product.name;
  let img = document.createElement("img");
  img.src = product.avatar;
  let txt = document.createElement("p");
  txt.innerText = product.desc;

  card.append(img, txt);
  articles.appendChild(card);

  return articles;
};

